#!/usr/bin/python3
str1 = "Holberton"
str2 = "School"
str3 = str1 + ' ' + str2
print(f"Welcome to {str3}!")
# The f-string format is used to concatenate the two strings with a space in between.
# The print function is used to print the concatenated string.
# The output will be:
# Welcome to Holberton School!